public class AS7sl
{
	public static double getCirArea(double rad)
	{
			return 3.14 * (rad * rad);
	}

	public static double getRecArea(double len, double wid)
	{
			return len * wid;
	}

	public static double getTriArea(double b, double h)
	{
			return b * h * 0.5;
	}
}
